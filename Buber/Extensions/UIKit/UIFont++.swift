import UIKit

extension UIFont {
    static func appRegular(_ size: CGFloat) -> UIFont { return UIFont(name: "Poppins-Regular", size: size)! }
    static func appMedium(_ size: CGFloat) -> UIFont { return UIFont(name: "Poppins-Medium", size: size)! }
    static func appBold(_ size: CGFloat) -> UIFont { return UIFont(name: "Poppins-Bold", size: size)! }
}

